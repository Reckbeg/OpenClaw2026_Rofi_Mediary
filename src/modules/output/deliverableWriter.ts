import * as fs from "fs";
import * as path from "path";
import * as os from "os";
import type {
  ActionArtifact,
  FollowUpTask,
  InterventionQueueItem,
  MediaryLoopOutput,
} from "@/src/types/mediary";

const DEFAULT_OUTPUT_DIR = path.join(os.homedir(), ".hermes", "deliverables");

function ensureDir(dirPath: string): void {
  if (!fs.existsSync(dirPath)) {
    fs.mkdirSync(dirPath, { recursive: true });
  }
}

function sanitizeFilename(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

function findQueueItem(
  queue: InterventionQueueItem[],
  employeeId: string,
): InterventionQueueItem | undefined {
  return queue.find((q) => q.employeeId === employeeId);
}

// ── HR Ops case ──────────────────────────────────────────────────────────────

function formatHrOpsCase(
  artifact: ActionArtifact,
  queueItem: InterventionQueueItem | undefined,
): string {
  const riskScore = queueItem?.riskScore ?? "N/A";
  const riskBucket = queueItem?.riskBucket ?? "N/A";
  const route = queueItem?.route ?? "Sustained High: HR Ops queue";
  const rationale = queueItem?.decisionRationale ?? artifact.body;
  const nextStep = queueItem?.nextStep ?? "";
  const followUpCadence = queueItem?.followUpCadence ?? "48-hour HR Ops review cycle";
  const team = queueItem?.team ?? "Unknown";

  return [
    `# ${artifact.title}`,
    "",
    `**Employee:** ${artifact.employeeName}`,
    `**Team:** ${team}`,
    `**Owner:** ${artifact.owner}`,
    `**Risk Score:** ${riskScore} (${riskBucket})`,
    `**Route:** ${route}`,
    "",
    "---",
    "",
    "## Trend & Rationale",
    "",
    rationale,
    "",
    "## Next Steps",
    "",
    nextStep
      ? `1. ${nextStep}`
      : "1. Review case packet with HR Ops lead.",
    "2. Confirm alignment with direct manager.",
    "3. Validate focus-block execution and workload redistribution.",
    "",
    "## Follow-Up Cadence",
    "",
    followUpCadence,
    "",
    "---",
    "",
    `*Generated by Mediary — ${new Date().toISOString().split("T")[0]}*`,
    "",
  ].join("\n");
}

