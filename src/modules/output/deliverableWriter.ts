// ── Follow-up checklist ──────────────────────────────────────────────────────

function formatFollowUpChecklist(tasks: FollowUpTask[]): string {
  const lines = [
    "# Follow-Up Task Checklist",
    "",
    `**Generated:** ${new Date().toISOString().split("T")[0]}`,
    "",
    "---",
    "",
  ];

  for (const task of tasks) {
    lines.push(`## ${task.employeeName} — ${task.owner}`);
    lines.push("");
    lines.push(`- **Due:** ${task.dueIn}`);
    lines.push(`- **Trigger:** ${task.trigger}`);
    lines.push(`- [ ] ${task.task}`);
    lines.push("");
  }

  lines.push("---");
  lines.push("");
  lines.push(`*Generated by Mediary — ${new Date().toISOString().split("T")[0]}*`);
  lines.push("");

  return lines.join("\n");
}

// ── Main export ──────────────────────────────────────────────────────────────

/**
 * Write all deliverable files from a completed Mediary loop run.
 *
 * Creates the output directory if it doesn't exist. Each action artifact
 * gets its own `.md` file, plus a standalone HR memo and a follow-up
 * checklist file.
 */
export function writeDeliverables(
  output: Pick<
    MediaryLoopOutput,
    "actionArtifacts" | "followUpTasks" | "hrMemo" | "interventionQueue"
  >,
  outputDir: string = DEFAULT_OUTPUT_DIR,
): string[] {
  ensureDir(outputDir);

  const writtenPaths: string[] = [];
  const queue = output.interventionQueue ?? [];

  // ── Action artifacts ──
  for (const artifact of output.actionArtifacts) {
    const queueItem = findQueueItem(queue, artifact.employeeId);
    let content: string;
    let prefix: string;

    switch (artifact.type) {
      case "hr_ops_case":
        content = formatHrOpsCase(artifact, queueItem);
        prefix = "hr-ops-case";
        break;
      case "manager_brief":
        content = formatManagerBrief(artifact, queueItem);
        prefix = "manager-brief";
        break;
      case "employee_nudge":
        content = formatEmployeeNudge(artifact, queueItem);
        prefix = "employee-nudge";
        break;
      case "focus_block_plan":
        content = formatFocusBlockPlan(artifact, queueItem);
        prefix = "focus-block-plan";
        break;
      default:
        content = `# ${artifact.title}\n\n${artifact.body}\n`;
        prefix = artifact.type;
    }

    const filename = `${sanitizeFilename(prefix)}-${sanitizeFilename(artifact.employeeName)}.md`;
    const filePath = path.join(outputDir, filename);
    fs.writeFileSync(filePath, content, "utf-8");
    writtenPaths.push(filePath);
  }

  // ── HR Memo ──
  if (output.hrMemo) {
    const memoPath = path.join(outputDir, "hr-memo.md");
    fs.writeFileSync(memoPath, formatHrMemo(output.hrMemo), "utf-8");
    writtenPaths.push(memoPath);
  }

  // ── Follow-up checklist ──
  if (output.followUpTasks && output.followUpTasks.length > 0) {
    const checklistPath = path.join(outputDir, "follow-up-checklist.md");
    fs.writeFileSync(
      checklistPath,
      formatFollowUpChecklist(output.followUpTasks),
      "utf-8",
    );
    writtenPaths.push(checklistPath);
  }

  return writtenPaths;
}
